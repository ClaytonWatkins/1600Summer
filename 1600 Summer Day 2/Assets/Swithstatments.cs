using UnityEngine;
using System.Collections;

public class Swithstatments : MonoBehaviour {

public health;

void displayHealthBarColor(){
	while (health){
		case >100%:
			set bar color to green;
			break;
		case >75%:
			set bar color to blue;
			break;
		case >50%:
			set bar color to orange;
			break;
		case >25%:
			set bar color to red and flash;
			break;
		case <0%:
			set bar color to black and start crumble animation;
			break;
	}
}
}